    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (jList1.getSelectedValue().equals("")) {
        } else {

            if (!kv1.containsKey(jList1.getSelectedValue()) && !kv2.containsKey(jList1.getSelectedValue())) {
                ChatAPerson a = new ChatAPerson();
                a.isAval(jList1.getSelectedValue());
                kv1.put(jList1.getSelectedValue(), a);
                kv1.get(jList1.getSelectedValue()).show();

            } else if (kv2.containsKey(jList1.getSelectedValue())) {
                kv1.put(jList1.getSelectedValue(), kv2.get(jList1.getSelectedValue()));
                kv2.get(jList1.getSelectedValue()).show();
                kv2.remove(jList1.getSelectedValue());
            }

        }

    }//GEN-LAST:event_jButton5ActionPerformed

