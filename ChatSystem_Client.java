    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        try {
            dos.writeUTF("exit");
            dos.flush();
        } catch (IOException ex) {
            System.err.println(ex.getMessage());
        } finally {
            try {
                dos.close();
                dis.close();
                u_socket.close();
                System.exit(0);
            } catch (IOException ex) {
                System.err.println(ex.getMessage());
            }
            System.exit(0);
        }
    }//GEN-LAST:event_formWindowClosing

