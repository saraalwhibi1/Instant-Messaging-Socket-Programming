    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        uName = jTextField3.getText();
        if (!"".equals(uName) && uName.length()<=10) {
            try {
                u_socket = new Socket(InetAddress.getByName("127.0.0.1"), 5111);
                dis = new DataInputStream(u_socket.getInputStream());
                dos = new DataOutputStream(u_socket.getOutputStream());
                dos.writeUTF("uAdd* " + uName);
                dos.flush();
                jButton3.setEnabled(false);
                jTextField3.setEnabled(false);
                msgListenThread.start();
                jButton4.setEnabled(true);

            } catch (UnknownHostException ex) {
                System.err.println(ex.getMessage());
            } catch (IOException ex) {
                System.err.println(ex.getMessage());
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Enter a username with length less than 10 and not null","Warning",JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton3ActionPerformed
    Thread msgListenThread = new Thread(new Runnable() {
        @Override
        public void run() {
            try {
                while (true) {

                    String recResponse = dis.readUTF();
                    if (recResponse.split(" ")[0].equals("pcMsg*")) {
                        String all = "";
                        int size = recResponse.split(" ").length;
                        for (int i = 2; i < size; i++) {
                            all = all + recResponse.split(" ")[i] + " ";
                        }
                        jTextArea1.append(recResponse.split(" ")[1] + " sent: " + all + ".\n************\n");
                    } else if (recResponse.split(" ")[0].equals("uName")) {//uName u1 u2
                        DefaultListModel uNames = new DefaultListModel();
                        jList1.setModel(uNames);
                        for (int u = 0; u < recResponse.split(" ").length - 1; u++) {
                            if (!recResponse.split(" ")[u + 1].equals(uName)) {
                                uNames.addElement(recResponse.split(" ")[u + 1]);
                            }
                        }
                        jList1.setModel(uNames);
                    } else if (recResponse.split(" ")[0].equals("pvMsg*")) {
                        String all = "";
                        int size = recResponse.split(" ").length;
                        for (int i = 2; i < size; i++) {
                            all = all + recResponse.split(" ")[i] + " ";
                        }
                        if (kv1.containsKey(recResponse.split(" ")[1])) {
                            kv1.get(recResponse.split(" ")[1]).jTextArea2.append(recResponse.split(" ")[1] + " sent: " + all + ".\n************\n");

                        } else if (!kv2.containsKey(recResponse.split(" ")[1])) {
                            ChatAPerson a = new ChatAPerson();
                            a.isAval(recResponse.split(" ")[1]);
                            kv1.put(recResponse.split(" ")[1], a);
                            kv1.get(recResponse.split(" ")[1]).show();
                            kv1.get(recResponse.split(" ")[1]).jTextArea2.append(recResponse.split(" ")[1] + " sent: " + all + ".\n************\n");

                        } else {

                            kv1.put(recResponse.split(" ")[1], kv2.get(recResponse.split(" ")[1]));
                            kv1.get(recResponse.split(" ")[1]).show();

                            kv1.get(recResponse.split(" ")[1]).jTextArea2.append(recResponse.split(" ")[1] + " sent: " + all + ".\n************\n");

                        }

                    }

                }
            } catch (IOException ex) {
                System.err.println(ex.getMessage());
            }
        }
    });
